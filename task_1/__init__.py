import math


def main(z, x, y):
    return (50 * (3 * y ** 3 - 17 * z - 35) ** 3 - 86 * x ** 4) / \
           (44 * (z ** 2 - x ** 3 - y) ** 4 - 1) + \
           math.sqrt((26 * (16 * x + y ** 2) ** 4 +
                      (z ** 3 / 72 + 48 * y ** 2 + y / 35) ** 5 / 20) /
                     ((z ** 3 / 69 - 94 * y ** 2 - 54 * x) ** 6 -
                      (math.atan(z ** 2 - z ** 3 - x)) ** 3))


print('%.2e' % main(-0.74, -0.37, 0.13))
